package br.niltonvasques.moneycontrol.view.chart;

import java.text.SimpleDateFormat;
import java.util.GregorianCalendar;

import org.afree.chart.AFreeChart;
import org.afree.chart.ChartFactory;
import org.afree.chart.axis.DateAxis;
import org.afree.chart.plot.XYPlot;
import org.afree.chart.renderer.xy.XYItemRenderer;
import org.afree.chart.renderer.xy.XYLineAndShapeRenderer;
import org.afree.data.time.Month;
import org.afree.data.time.TimeSeries;
import org.afree.data.time.TimeSeriesCollection;
import org.afree.data.xy.XYDataset;
import org.afree.graphics.SolidColor;
import org.afree.ui.RectangleInsets;

import br.niltonvasques.moneycontrol.R;

import android.content.Context;
import android.graphics.Color;
import android.text.GetChars;

/**
 * TimeSeriesChartDemo01View
 */
public class TimeSeriesChartView extends DemoView {

    /**
     * constructor
     * @param context
     */
    public TimeSeriesChartView(Context context, String title, TimeSeries[] series) {
        super(context);

        final AFreeChart chart = createChart(title, createDataset(series));

        setChart(chart);
    }

    /**
     * Creates a chart.
     *
     * @param dataset  a dataset.
     *
     * @return A chart.
     */
    private AFreeChart createChart(String title, XYDataset dataset) {

        AFreeChart chart = ChartFactory.createTimeSeriesChart(
    		title,  // title
            getResources().getString(R.string.axis_date_label),             // x-axis label
            getResources().getString(R.string.axis_price_label),   // y-axis label
            dataset,            // data
            true,               // create legend?
            true,               // generate tooltips?
            false               // generate URLs?
        );

        chart.setBackgroundPaintType(new SolidColor(Color.WHITE));

        XYPlot plot = (XYPlot) chart.getPlot();
        plot.setBackgroundPaintType(new SolidColor(Color.LTGRAY));
        plot.setDomainGridlinePaintType(new SolidColor(Color.WHITE));
        plot.setRangeGridlinePaintType(new SolidColor(Color.WHITE));
        plot.setAxisOffset(new RectangleInsets(5.0, 5.0, 5.0, 5.0));
        plot.setDomainCrosshairVisible(true);
        plot.setRangeCrosshairVisible(true);

        XYItemRenderer r = plot.getRenderer();
        if (r instanceof XYLineAndShapeRenderer) {
            XYLineAndShapeRenderer renderer = (XYLineAndShapeRenderer) r;
            renderer.setBaseShapesVisible(true);
            renderer.setBaseShapesFilled(true);
            renderer.setDrawSeriesLineAsPath(true);
        }

        DateAxis axis = (DateAxis) plot.getDomainAxis();
        axis.setDateFormatOverride(new SimpleDateFormat("MMM-yyyy"));

        return chart;

    }

    /**
     * Creates a dataset, consisting of two series of monthly data.
     *
     * @return The dataset.
     */
    public XYDataset createDataset(TimeSeries[] series) {

        TimeSeriesCollection dataset = new TimeSeriesCollection();
        for (TimeSeries timeSeries : series) {
			dataset.addSeries(timeSeries);
		}

        return dataset;

    }
}
